#include <cstdio>
int main(){
	// freopen("in","r",stdin);
	long long N; scanf("%lld",&N);
	long long ans = 0;
	if(N <= 9){
		ans = N;
	}else if(N <= 99){
		ans = 9 + (N-9)*2;
	}else if(N <= 999){
		ans = 9 + 90*2 + (N-99)*3;
	}else if(N <= 9999){
		ans = 9 + 90*2 + 900*3 + (N-999)*4;
	}else if(N <= 99999){
		ans = 9 + 90*2 + 900*3 + 9000*4 + (N-9999)*5;
	}else if(N <= 999999){
		ans = 9 + 90*2 + 900*3 + 9000*4 + 90000*5 + (N-99999)*6;
	}else if(N <= 9999999){
		ans = 9 + 90*2 + 900*3 + 9000*4 + 90000*5 + 900000*6 + (N-999999)*7;
	}else if(N <= 99999999LL){
		ans = 9 + 90*2 + 900*3 + 9000*4 + 90000*5 + 900000*6 + 9000000LL*7LL + (N-9999999)*8;
	}else if(N <= 999999999LL){
		ans = 9 + 90*2 + 900*3 + 9000*4 + 90000*5 + 900000*6 + 9000000LL*7LL + 90000000LL*8LL + (N-99999999)*9;
	}else if(N == 1000000000){
		ans = 9 + 90*2 + 900*3 + 9000*4 + 90000*5 + 900000*6 + 9000000LL*7LL + 90000000LL*8LL + 900000000LL*9LL + 10;
	}
	printf("%lld\n",ans);
	return 0;
}